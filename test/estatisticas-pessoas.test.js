const estatisticas = require('../src/estatisticas-pessoas.js')

describe('O modulo de estatisticas', () => {

  it('mostra quem tem o maior salario', () => {
    const maiorSalario = estatisticas.maiorSalario()

    expect(maiorSalario).toEqual(6200)
  })

  it('mostra quem tem o pet chamado Vieirinha', () => {
    const pessoaDaVieirinha = estatisticas.possuiPetChamado('Vieirinha')

    expect(pessoaDaVieirinha.nome).toEqual('Vieira')
    expect(pessoaDaVieirinha.idade).toEqual(31)
    expect(pessoaDaVieirinha.salario).toEqual(6200)
  })

  it('mostra os nomes de quem tem nenhum pet', () => {
    const pessoasSemPets = estatisticas.possuiNenhumPet()

    expect(pessoasSemPets).toEqual(['Silva', 'Cruz'])
  })
})
